CREATE INDEX IF NOT EXISTS idx_student_total_food_summaries_class_total_food ON student_total_food_summaries(class_id, total_food DESC);
CREATE INDEX IF NOT EXISTS idx_shop_items_class_id ON shop_items(class_id);
CREATE INDEX IF NOT EXISTS idx_exchange_records_class_id ON exchange_records(class_id);
CREATE INDEX IF NOT EXISTS idx_exchange_records_student_id ON exchange_records(student_id);
