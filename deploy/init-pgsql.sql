-- 班级宠物屋 PostgreSQL 初始化脚本
-- 执行前请先连接到目标数据库，例如：
--   CREATE DATABASE class_pet_house;
--   \c class_pet_house
--
-- 当前脚本覆盖现行业务表：
-- users, classes, students, groups, score_rules, history,
-- student_total_food_summaries, shop_items, exchange_records, licenses
--
-- 不包含历史临时/备份表，例如 users_backup。

BEGIN;

-- 统一维护 updated_at 的触发器函数
CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TABLE IF NOT EXISTS users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(50) NOT NULL UNIQUE,
    password_hash VARCHAR(255) NOT NULL,
    activation_code VARCHAR(100),
    is_activated BOOLEAN NOT NULL DEFAULT FALSE,
    is_admin BOOLEAN NOT NULL DEFAULT FALSE,
    settings JSONB NOT NULL DEFAULT '{}'::jsonb,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_users_settings_object CHECK (jsonb_typeof(settings) = 'object')
);

CREATE TABLE IF NOT EXISTS classes (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL,
    name VARCHAR(100) NOT NULL DEFAULT '默认班级',
    system_name VARCHAR(100) NOT NULL DEFAULT '石榴果宠物屋',
    theme VARCHAR(50) NOT NULL DEFAULT 'pink',
    growth_stages JSONB NOT NULL DEFAULT '[0,5,10,20,30,45,60,75,90,100]'::jsonb,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_classes_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT chk_classes_growth_stages_array CHECK (jsonb_typeof(growth_stages) = 'array'),
    CONSTRAINT chk_classes_sort_order_non_negative CHECK (sort_order >= 0)
);

CREATE TABLE IF NOT EXISTS groups (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    class_id INTEGER NOT NULL,
    name VARCHAR(50) NOT NULL,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_groups_class FOREIGN KEY (class_id) REFERENCES classes(id) ON DELETE CASCADE,
    CONSTRAINT chk_groups_sort_order_non_negative CHECK (sort_order >= 0)
);

CREATE TABLE IF NOT EXISTS students (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    class_id INTEGER NOT NULL,
    name VARCHAR(50) NOT NULL,
    pet_type VARCHAR(50),
    pet_name VARCHAR(50),
    food_count INTEGER NOT NULL DEFAULT 0,
    food_reset_at TIMESTAMP,
    total_food_earned INTEGER NOT NULL DEFAULT 0,
    badges JSONB NOT NULL DEFAULT '[]'::jsonb,
    current_badges_count INTEGER NOT NULL DEFAULT 0,
    total_badges_earned INTEGER NOT NULL DEFAULT 0,
    sort_order INTEGER NOT NULL DEFAULT 0,
    group_id INTEGER,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_students_class FOREIGN KEY (class_id) REFERENCES classes(id) ON DELETE CASCADE,
    CONSTRAINT fk_students_group FOREIGN KEY (group_id) REFERENCES groups(id) ON DELETE SET NULL,
    CONSTRAINT chk_students_badges_array CHECK (jsonb_typeof(badges) = 'array'),
    CONSTRAINT chk_students_food_count_non_negative CHECK (food_count >= 0),
    CONSTRAINT chk_students_total_food_non_negative CHECK (total_food_earned >= 0),
    CONSTRAINT chk_students_current_badges_non_negative CHECK (current_badges_count >= 0),
    CONSTRAINT chk_students_total_badges_non_negative CHECK (total_badges_earned >= 0),
    CONSTRAINT chk_students_sort_order_non_negative CHECK (sort_order >= 0)
);

CREATE TABLE IF NOT EXISTS score_rules (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    class_id INTEGER NOT NULL,
    name VARCHAR(50) NOT NULL,
    icon VARCHAR(50) NOT NULL DEFAULT '⭐',
    value INTEGER NOT NULL,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_score_rules_class FOREIGN KEY (class_id) REFERENCES classes(id) ON DELETE CASCADE,
    CONSTRAINT chk_score_rules_sort_order_non_negative CHECK (sort_order >= 0)
);

CREATE TABLE IF NOT EXISTS history (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    class_id INTEGER NOT NULL,
    student_id INTEGER NOT NULL,
    rule_id INTEGER,
    rule_name VARCHAR(50),
    value INTEGER NOT NULL DEFAULT 0,
    type VARCHAR(20) NOT NULL DEFAULT 'score',
    is_revoked BOOLEAN NOT NULL DEFAULT FALSE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_history_class FOREIGN KEY (class_id) REFERENCES classes(id) ON DELETE CASCADE,
    CONSTRAINT fk_history_student FOREIGN KEY (student_id) REFERENCES students(id) ON DELETE CASCADE,
    CONSTRAINT chk_history_type CHECK (type IN ('score', 'graduate', 'exchange', 'revoke'))
);

CREATE TABLE IF NOT EXISTS student_total_food_summaries (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    class_id INTEGER NOT NULL,
    student_id INTEGER NOT NULL,
    rule_id INTEGER NOT NULL,
    rule_name VARCHAR(50) NOT NULL,
    award_count INTEGER NOT NULL DEFAULT 0,
    total_food INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_student_total_food_summaries_class FOREIGN KEY (class_id) REFERENCES classes(id) ON DELETE CASCADE,
    CONSTRAINT fk_student_total_food_summaries_student FOREIGN KEY (student_id) REFERENCES students(id) ON DELETE CASCADE,
    CONSTRAINT uq_student_total_food_summaries_student_rule UNIQUE (class_id, student_id, rule_id),
    CONSTRAINT chk_student_total_food_summaries_award_count_positive CHECK (award_count > 0),
    CONSTRAINT chk_student_total_food_summaries_total_food_positive CHECK (total_food > 0)
);

CREATE TABLE IF NOT EXISTS shop_items (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    class_id INTEGER NOT NULL,
    name VARCHAR(50) NOT NULL,
    description VARCHAR(200) NOT NULL DEFAULT '',
    icon VARCHAR(50) NOT NULL DEFAULT '🎁',
    price INTEGER NOT NULL DEFAULT 1,
    stock INTEGER NOT NULL DEFAULT -1,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_shop_items_class FOREIGN KEY (class_id) REFERENCES classes(id) ON DELETE CASCADE,
    CONSTRAINT chk_shop_items_price_positive CHECK (price >= 1),
    CONSTRAINT chk_shop_items_stock_valid CHECK (stock >= -1)
);

CREATE TABLE IF NOT EXISTS exchange_records (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    class_id INTEGER NOT NULL,
    student_id INTEGER NOT NULL,
    item_id INTEGER,
    item_name VARCHAR(50) NOT NULL,
    cost INTEGER NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_exchange_records_class FOREIGN KEY (class_id) REFERENCES classes(id) ON DELETE CASCADE,
    CONSTRAINT fk_exchange_records_student FOREIGN KEY (student_id) REFERENCES students(id) ON DELETE CASCADE,
    CONSTRAINT chk_exchange_records_cost_non_negative CHECK (cost >= 0)
);

CREATE TABLE IF NOT EXISTS licenses (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(100) NOT NULL UNIQUE,
    is_used BOOLEAN NOT NULL DEFAULT FALSE,
    used_by INTEGER,
    used_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_licenses_user FOREIGN KEY (used_by) REFERENCES users(id) ON DELETE SET NULL
);

-- 常用索引
CREATE INDEX IF NOT EXISTS idx_classes_user_id ON classes(user_id);
